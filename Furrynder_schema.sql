USE Furrynder;

CREATE TABLE Locations(
Location_id INTEGER UNSIGNED AUTO_INCREMENT PRIMARY KEY,
Location_name VARCHAR(255) NOT NULL
) Engine=InnoDB DEFAULT CHARSET=utf8mb4;

